package com.pojo;

public class Marks {


    int Tamil;
    int English;

    public Marks(int tamil, int english) {
        Tamil = tamil;
        English = english;
    }


    public int getTamil() {
        return Tamil;
    }

    public void setTamil(int tamil) {
        Tamil = tamil;
    }

    public int getEnglish() {
        return English;
    }

    public void setEnglish(int english) {
        English = english;
    }


}
